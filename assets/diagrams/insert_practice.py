def section(n):
    title, body = S[n]
    img = f"../assets/diagrams/{IMG[n]}"
    return (f"## 실습 \u2014 {title}\n\n"
            f"![{ALT[n]}]({img})\n\n"
            f"{body}\n")

def main():
    for n, fname in FILES.items():
        path = os.path.join(MAN, fname)
        txt = open(path, encoding="utf-8").read()
        if "## 실습 \u2014 코봇 키우기" in txt:
            print("skip(exists)", fname); continue
        lines = txt.split("\n")
        # find last *** line index
        idx = None
        for i in range(len(lines)-1, -1, -1):
            if lines[i].strip() == "***":
                idx = i; break
        sec = section(n)
        block = sec.split("\n")
        # ensure blank line before section
        new = lines[:idx] + [""] + block + [""] + lines[idx:]
        open(path, "w", encoding="utf-8").write("\n".join(new))
